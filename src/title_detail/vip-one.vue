      1、无醇葡萄酒的定义

     无醇葡萄酒是一种酒精度低于0.5度的发酵型葡萄酒，其加工过程首先将葡萄发酵成葡萄酒，再经过脱醇工艺将酒精去除而成，所以无醇葡萄酒除了没有酒精，几乎和葡萄酒一模一样。与葡萄汁相比，无醇葡萄酒经过长时间的浸渍、发酵等过程，其风味更加浓郁、口感更加复杂、功能成分更多，同时经过发酵将糖分充分转化。其无酒精、低糖、风味好及富含功能成分的特质符合当前的消费趋势，是一种健康而时尚的饮品。由于其不含酒精，特别适合驾驶人士、酒精不耐受人群及宴饮聚会等。

